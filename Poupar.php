<?php

Class Poupar {

    private $nomePoupar;
    private $descricaoPoupar;
    private $valorPoupar;
    private $dataInicioPoupar;
    private $dataFinalPoupar;
    private $statusPoupar;

    function getNomePoupar() {
        return $this->nomePoupar;
    }

    function setNomePoupar($nomePoupar) {
        $this->nomePoupar = $nomePoupar;
    }

    function getDescricaoPoupar() {
        return $this->descricaoPoupar;
    }

    function setDescricaoPoupar($descricaoPoupar) {
        $this->descricaoPoupar = $descricaoPoupar;
    }

    function getValorPoupar() {
        return $this->valorPoupar;
    }

    function setValorPoupar($valorPoupar) {
        $this->valorPoupar = $valorPoupar;
    }

    function getDataInicioPoupar() {
        return $this->dataInicioPoupar;
    }

    function setDataInicioPoupar($dataInicioPoupar) {
        $this->dataInicioPoupar = $dataInicioPoupar;
    }

    function getDataFinalPoupar() {
        return $this->dataFinalPoupar;
    }

    function setDataFinalPoupar($dataFinalPoupar) {
        $this->dataFinalPoupar = $dataFinalPoupar;
    }
    function getStatusPoupar() {
        return $this->statusPoupar;
    }

    function setStatusPoupar($statusPoupar) {
        $this->statusPoupar = $statusPoupar;
    }


}
